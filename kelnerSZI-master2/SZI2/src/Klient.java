
public class Klient {

	
	public String jakoscJedzenia;
	public String czystoscRestauracji;
	public String czasOczekiwania;
	public String jakoscObslugi;
	public String cenaPosilku;

	public Klient(String jakoscJedzenia, String czystoscRestauracji, String czasOczekiwania, String jakoscObslugi, String cenaPosilku) {
		this.jakoscJedzenia = jakoscJedzenia;
		this.czystoscRestauracji = czystoscRestauracji;
		this.czasOczekiwania = czasOczekiwania;
		this.jakoscObslugi = jakoscObslugi;
		this.cenaPosilku = cenaPosilku;
	}
}
